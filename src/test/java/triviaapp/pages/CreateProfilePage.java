  public CreateProfilePage (AppiumDriver driver) {
    super(driver);
  }

  public String getCreateProfileHeader(){
    return createProfileHeader.getText();
  }

  public void waitCreateProfile(){
    waitElemVisibility(nicknameInput);
  }


  public void verifyCreateProfileHeader(){
    Assert.assertEquals(getCreateProfileHeader(), LoginTestsData.CREATE_PROFILE_HEADER);
  }

  public void populateNickname(String nickname){
    nicknameInput.click();
    nicknameInput.sendKeys(nickname);
    Log.info(String.format("Type nickname %s in the field", nickname));
  }

  public void verifyUniqueNickname(){
    Assert.assertTrue(nicknameCheck.isDisplayed());
    logAssert(nicknameCheck, "Nickname is unique", "Nickname is not unique");
  }

  public void chooseState(){
    waitElemVisibility(stateDropdown);
    stateDropdown.click();
    waitElemVisibility(stateDropdownOption);
    stateDropdownOption.click();
    Log.info("Avatar is selected");
  }

  public void chooseAvatar(){
    waitElemVisibility(avatarLabel);
    avatarOption.click();
    Log.info("Avatar is selected");
  }

  public WalkthroughIntroPage clickContinueButton(){
    waitElemVisibility(continueButton);
    continueButton.click();
    Log.info("Clicked register continue button");
    return new WalkthroughIntroPage(driver);
  }

